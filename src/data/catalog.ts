function cleanDisplayName(raw: string): string {
  // Remove item code prefix, trailing UOM info after last underscore
  let name = raw.replace(/^[A-Z]{2,3}_\d+[\s.]*\d*\s*/, "");
  // Remove trailing _1PC, _1KG, etc.
  name = name.replace(/_\d*\s*(PC|PCS|KG|G|L|ML|PACK|ROLL|GAL|CAN|BOX|LITER|BOTTLE)[S]?[/\w]*$/i, "");
  name = name.replace(/_/g, " ").trim();
  // Title case
  return name
    .split(" ")
    .map((w) => {
      if (["W/", "W/O", "&", "AND", "OF", "+"].includes(w.toUpperCase())) return w.toLowerCase();
      return w.charAt(0).toUpperCase() + w.slice(1).toLowerCase();
    })
    .join(" ")
    .replace(/\b(Dot|Uht|Xxl|Pos)\b/gi, (m) => m.toUpperCase());
}

function mockPrice(cat: string): number {
  const base: Record<string, [number, number]> = {
    COM: [35, 180],
    MER: [120, 650],
    OFS: [25, 90],
    PKG: [3, 45],
    RMC: [45, 550],
    RMS: [30, 450],
    RSC: [80, 120],
    STK: [5, 15],
    UTS: [35, 180],
  };
  const [lo, hi] = base[cat] || [20, 200];
  return Math.round((lo + Math.random() * (hi - lo)) / 5) * 5;
}

