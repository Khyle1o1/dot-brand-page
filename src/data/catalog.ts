// Seed with deterministic prices using item code as seed
function seededPrice(code: string, cat: string): number {
  let hash = 0;
  for (let i = 0; i < code.length; i++) hash = ((hash << 5) - hash + code.charCodeAt(i)) | 0;
  const base: Record<string, [number, number]> = {
    COM: [35, 180], MER: [120, 650], OFS: [25, 90], PKG: [3, 45],
    RMC: [45, 550], RMS: [30, 450], RSC: [80, 120], STK: [5, 15], UTS: [35, 180],
  };
  const [lo, hi] = base[cat] || [20, 200];
  const norm = (Math.abs(hash) % 1000) / 1000;
  return Math.round((lo + norm * (hi - lo)) / 5) * 5;
}

export const catalogItems: CatalogItem[] = RAW_ITEMS.split("\n").map((line, i) => {
  const [codeRaw, desc, uom] = line.split("|");
  const item_code = codeRaw.trim();
  const category_code = item_code.split("_")[0];
  const category_name = CATEGORY_MAP[category_code] || category_code;
  const display_name = cleanDisplayName(desc.trim());

  return {
    id: `item-${i + 1}`,
    item_code,
    name: desc.trim(),
    description: desc.trim(),
    display_name,
    uom: uom.trim(),
    category_code,
    category_name,
    is_active: true,
    is_orderable: true,
    price: seededPrice(item_code, category_code),
    image_url: null,
    sort_order: i,
  };
});

export const categories = Object.entries(CATEGORY_MAP).map(([code, name]) => ({ code, name }));

export const uomOptions = [...new Set(catalogItems.map((i) => i.uom))].sort();
